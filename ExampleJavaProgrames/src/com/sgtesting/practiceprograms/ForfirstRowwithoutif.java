package com.sgtesting.practiceprograms;

public class ForfirstRowwithoutif {

	public static void main(String[] args) {
		int b[][]= {{2,3,4},{4,5,6},{3,4,5}};
		for(int i=0;i<=0;i++)
		{
			for(int c=0;c<=b[i].length-1;c++)
			{				
				System.out.print(b[i][c]+" ");				
			}
			System.out.println();
		}

	}

}
